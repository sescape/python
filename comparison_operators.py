# Comparison Operators
## They are used in expressions with operands on either side.
## Expressions with comparison operators evaluate to either True or False

# > Greater than
100 > 10
# evaluates to True

# < Less than
100 < 10
# evaluates to False

# >= Greater than or equal to 
2 <= 2 
# evaluates to True

# <= Less than or equal to 
2 <= 4
# evaluates to True

# == Equal
6 == 9
# evaluates to False

# != Not equal
4 != 5
# evaluates to True
