# Logical Operators
## Like comparison operators, logical operators also evaluates to True or False

# and 
True and True
# evaluates to True
1 == 1 and 2 == 2
# evaluates to True

# or 
True or False
# evaluates to True
1 == 3 or 3 == 3
# evaulates to True

# not 
not True
# evaluates to False
not 1 == 1
# evaluates to False
